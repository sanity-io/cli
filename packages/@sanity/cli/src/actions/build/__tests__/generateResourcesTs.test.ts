import {describe, expect, test} from 'vitest'

import {generateResourcesTs} from '../writeSanityRuntime.js'

describe('#generateResourcesTs', () => {
  test('returns empty resources module when called with undefined', () => {
    const result = generateResourcesTs(undefined)
    expect(result).toContain(
      `export const resources = {} satisfies Record<string, DocumentResource>`,
    )
    expect(result).not.toContain('RegisteredResources')
  })

  test('returns empty resources module when called with empty object', () => {
    const result = generateResourcesTs({})
    expect(result).toContain(
      `export const resources = {} satisfies Record<string, DocumentResource>`,
    )
    expect(result).not.toContain('RegisteredResources')
  })

  test('returns module with resources and RegisteredResources augmentation', () => {
    const result = generateResourcesTs({
      default: {dataset: 'production', projectId: 'proj-a'},
      secondary: {dataset: 'staging', projectId: 'proj-b'},
    })
    expect(result).toContain('"default": {projectId: "proj-a", dataset: "production"}')
    expect(result).toContain('"secondary": {projectId: "proj-b", dataset: "staging"}')
    expect(result).toContain('RegisteredResources')
    expect(result).toContain('"default": true')
    expect(result).toContain('"secondary": true')
    expect(result).toContain('satisfies Record<string, DocumentResource>')
  })

  test('includes auto-generated header comment', () => {
    expect(generateResourcesTs(undefined)).toContain('auto-generated by sanity CLI')
    expect(generateResourcesTs({default: {dataset: 'd', projectId: 'p'}})).toContain(
      'auto-generated by sanity CLI',
    )
  })

  test('imports DocumentResource type in all cases', () => {
    expect(generateResourcesTs(undefined)).toContain(
      `import type {DocumentResource} from '@sanity/sdk'`,
    )
    expect(generateResourcesTs({default: {dataset: 'd', projectId: 'p'}})).toContain(
      `import type {DocumentResource} from '@sanity/sdk'`,
    )
  })
})
